# Определить индексы элементов массива (списка), значения которых принадлежат заданному диапазону (т.е. не меньше заданного минимума и не больше заданного максимума). Ввод:  [-5, 9, 0, 3, -1, -2, 1, 4, -2, 10, 2, 0, -9, 8, 10, -9, 0, -5, -5, 7] Вывод: [1, 9, 13, 14, 19] 9 10 8 10 7

sp = [-5, 9, 0, 3, -1, -2, 1, 4, -2, 10, 2, 0, -9, 8, 10, -9, 0, -5, -5, 7] 
min_sp = 6
max_sp = 11
result = []
for i in range(len(sp)):
    if min_sp < sp[i] < max_sp:
        result.append(i)
print(result)
